// Category mapping for easy filtering
const CATEGORY_MAPPING = {
  "pizza": ["Pizza"],
  "burger": ["Burgers"],
  "taco": ["Tacos", "Mexican"],
  "biryani": ["Biryani", "Rice"],
  "shawarma": ["Shawarma", "Middle Eastern"],
  "momos": ["Momos", "Dumplings"],
  "pasta": ["Pasta", "Italian"],
  "sandwich": ["Sandwiches"],
  "noodles": ["Noodles", "Asian"]
};

// Function to get restaurant data by category
function getRestaurantsByCategory(categoryId) {
  const categoryNames = CATEGORY_MAPPING[categoryId] || [];
  const results = {};
  
  for (const [restaurantName, items] of Object.entries(RESTAURANT_DATA)) {
    const filteredItems = items.filter(item => 
      categoryNames.some(cat => 
        item.Category && item.Category.toLowerCase().includes(cat.toLowerCase())
      )
    );
    
    if (filteredItems.length > 0) {
      results[restaurantName] = filteredItems;
    }
  }
  
  return results;
}

// Function to get all items for a specific restaurant
function getRestaurantData(restaurantName) {
  return RESTAURANT_DATA[restaurantName] || [];
}

// Function to search items across all restaurants
function searchItems(query) {
  const results = [];
  const searchTerm = query.toLowerCase();
  
  for (const [restaurantName, items] of Object.entries(RESTAURANT_DATA)) {
    const matchingItems = items.filter(item =>
      item.ItemName.toLowerCase().includes(searchTerm) ||
      item.Category.toLowerCase().includes(searchTerm) ||
      (item.AdditionalInfo && item.AdditionalInfo.toLowerCase().includes(searchTerm))
    );
    results.push(...matchingItems);
  }
  
  return results;
}

// Function to compare prices for a specific item across restaurants
function compareItemPrices(itemName) {
  const results = [];
  const searchTerm = itemName.toLowerCase();
  
  for (const [restaurantName, items] of Object.entries(RESTAURANT_DATA)) {
    const matchingItems = items.filter(item =>
      item.ItemName.toLowerCase().includes(searchTerm)
    );
    results.push(...matchingItems);
  }
  
  // Sort by price (ascending)
  return results.sort((a, b) => (a.Price || 0) - (b.Price || 0));
}

